import fs from 'fs';
import path from 'path';
import { execSync } from 'child_process';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');

const packageJsonPath = path.join(rootDir, 'package.json');
const packageLockPath = path.join(rootDir, 'package-lock.json');
const backendVersionPath = path.join(rootDir, 'backend', 'version.json');
const frontendVersionOut = path.join(rootDir, 'src', 'version.ts');

const args = process.argv.slice(2);
const getArg = (name) => {
  const idx = args.indexOf(name);
  if (idx === -1) return null;
  const val = args[idx + 1];
  if (!val || val.startsWith('--')) return '';
  return val;
};

const target = (getArg('--target') || 'changed').toLowerCase();
const bumpType = (getArg('--type') || 'patch').toLowerCase();

const readJson = (filePath) => JSON.parse(fs.readFileSync(filePath, 'utf-8'));
const writeJson = (filePath, data) => fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf-8');

const bumpVersion = (version, kind) => {
  const [major, minor, patch] = version.split('.').map((v) => Number(v || 0));
  if (Number.isNaN(major) || Number.isNaN(minor) || Number.isNaN(patch)) {
    throw new Error(`Invalid version: ${version}`);
  }
  if (kind === 'major') return `${major + 1}.0.0`;
  if (kind === 'minor') return `${major}.${minor + 1}.0`;
  return `${major}.${minor}.${patch + 1}`;
};

const getChangedFiles = () => {
  const staged = execSync('git diff --cached --name-only', { encoding: 'utf-8' }).trim();
  if (staged) return staged.split('\n').filter(Boolean);
  const working = execSync('git diff --name-only', { encoding: 'utf-8' }).trim();
  if (working) return working.split('\n').filter(Boolean);
  return [];
};

const isFrontendChange = (file) => {
  if (file.startsWith('src/')) return true;
  if (file.startsWith('public/')) return true;
  const frontendFiles = [
    'index.html',
    'vite.config.ts',
    'default_shadcn_theme.css',
    'postcss.config.mjs',
    'env.d.ts',
  ];
  return frontendFiles.includes(file);
};

const isBackendChange = (file) => {
  if (file.startsWith('backend/')) return true;
  return file === 'server.js';
};

const files = target === 'changed' ? getChangedFiles() : [];

let bumpFrontend = false;
let bumpBackend = false;

if (target === 'frontend') bumpFrontend = true;
if (target === 'backend') bumpBackend = true;
if (target === 'all') {
  bumpFrontend = true;
  bumpBackend = true;
}
if (target === 'changed') {
  bumpFrontend = files.some(isFrontendChange);
  bumpBackend = files.some(isBackendChange);
}

if (!bumpFrontend && !bumpBackend) {
  console.log('No version bump needed.');
  process.exit(0);
}

const pkg = readJson(packageJsonPath);
const backend = readJson(backendVersionPath);

let updated = false;
if (bumpFrontend) {
  const next = bumpVersion(pkg.version || '0.0.0', bumpType);
  pkg.version = next;
  updated = true;
  console.log(`Bumped frontend version to ${next}`);
}

if (bumpBackend) {
  const next = bumpVersion(backend.version || '0.0.0', bumpType);
  backend.version = next;
  updated = true;
  console.log(`Bumped backend version to ${next}`);
}

if (updated) {
  writeJson(packageJsonPath, pkg);
  if (fs.existsSync(packageLockPath)) {
    const lock = readJson(packageLockPath);
    lock.version = pkg.version;
    if (lock.packages && lock.packages['']) {
      lock.packages[''].version = pkg.version;
    }
    writeJson(packageLockPath, lock);
  }
  writeJson(backendVersionPath, backend);

  const frontendVersion = pkg.version || '0.0.0';
  const backendVersion = backend.version || '0.0.0';
  const contents = `// Auto-generated by scripts/bump-versions.js
export const frontendVersion = '${frontendVersion}';
export const backendVersion = '${backendVersion}';
`;
  fs.writeFileSync(frontendVersionOut, contents, 'utf-8');

  const addPaths = ['package.json', 'package-lock.json', 'backend/version.json', 'src/version.ts']
    .filter((p) => fs.existsSync(path.join(rootDir, p)));
  if (addPaths.length) {
    execSync(`git add -- ${addPaths.map((p) => `"${p}"`).join(' ')}`, { stdio: 'ignore' });
  }
}
