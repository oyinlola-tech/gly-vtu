import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');

const packageJsonPath = path.join(rootDir, 'package.json');
const backendVersionPath = path.join(rootDir, 'backend', 'version.json');
const frontendVersionOut = path.join(rootDir, 'src', 'version.ts');

const readJson = (filePath) => JSON.parse(fs.readFileSync(filePath, 'utf-8'));

const pkg = readJson(packageJsonPath);
const backend = readJson(backendVersionPath);

const frontendVersion = pkg.version || '0.0.0';
const backendVersion = backend.version || '0.0.0';

const contents = `// Auto-generated by scripts/sync-versions.js
export const frontendVersion = '${frontendVersion}';
export const backendVersion = '${backendVersion}';
`;

fs.writeFileSync(frontendVersionOut, contents, 'utf-8');
console.log(`Synced versions. Frontend: ${frontendVersion}, Backend: ${backendVersion}`);
